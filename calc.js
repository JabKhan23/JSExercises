function subCalc(num1, num2) {
    return num1 - num2;
}

function addCalc(num1, num2) {
    return num1 + num2;
}

function mulCalc(num1, num2) {
    return num1 * num2;
}

function divCalc(num1, num2) {
    return num1 / num2;
}

console.log(addCalc(10,10));
console.log(mulCalc(10,10));
console.log(subCalc(10,10));
console.log(divCalc(10,10));